public class DemoDouble{
    public static void main(String[] args){

        double d1 = 0.3;  //if no notation, default it is double value
        double d2 = 0.1d; //d is the notation of double
        double d3 = d1 + d2;
        System.out.println(d3);  //0.4

        double d4 = 19999.12315120598122;
        System.out.println(d4);  //19999.123151205982

        float f1 = 0.2f; //0.2 is default as double type, f is the notation of float, 0.2f is a float type which can be assigned to a float type variable.
        float f2 = 199999.194520871409325234f;
        System.out.println(f2);  //199999.19

      

    }
}